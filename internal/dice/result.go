package dice

import (
	"fmt"
	"strings"
)

// Result is the fully evaluated outcome of a dice expression.
type Result struct {
	Expression string // original raw expression

	Rolls    []int // initial + post-reroll/explode values
	Rerolls  []int // values generated by rerolls
	Exploded []int // values generated by explosions

	Kept    []int // values kept after keep/drop
	Dropped []int // values removed by keep/drop

	Total     int    // final total after additive modifiers
	Successes int    // success threshold count (if any)
	Verbose   string // human-readable breakdown (filled in verbose.go)
}

// MultiRollResult represents N independent evaluations of the same expression.
// This is returned by Engine.Evaluate() when count > 1.
type MultiRollResult struct {
	Expression string   `json:"expression"`
	Rolls      []Result `json:"rolls"`
	Summary    string   `json:"summary,omitempty"`
}

// FormatMultiExpression ensures a multi-roll expression is displayed with its
// `rolls=N` suffix exactly once.
func FormatMultiExpression(expr string, count int) string {
	expr = strings.TrimSpace(expr)
	if strings.Contains(expr, "rolls=") {
		return expr
	}
	if expr == "" {
		return fmt.Sprintf("rolls=%d", count)
	}
	return fmt.Sprintf("%s rolls=%d", expr, count)
}
